/*Day 6, the usage of space as a modifier in prinf function*/
#include <stdio.h>
#include <stdlib.h>

int main(){
    float w,x,y,z;
    w=-3.14159f;
    x=-1.732f;
    y=1.41421f;
    z=0.47712f;
    printf("w=% f\n",w);
    printf("x=% f\n",x);
    printf("y=% f\n",y);
    printf("z=% f\n",z);
    system("pause");
    return 0;
}