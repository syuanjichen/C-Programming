/*Day 6, modifier + in printf function*/
#include <stdio.h>
#include <stdlib.h>

int main(){
    float p,q,r,s;
    p=-2.71828;
    q=6.8742;
    r=-1.73254;
    s=3.23135;
    printf("p=%+f\n",p);
    printf("q=%+f\n",q);
    printf("r=%+f\n",r);
    printf("s=%+f\n",s);
    system("pause");
    return 0;
}