/*Day 4, unsigned integers, aka positive integers*/
#include <stdio.h>
#include <stdlib.h>

int main(){
    unsigned short int x=65535;/*[0,65535]*/
    unsigned long int y=4294967295;/*[0,4294967295]*/
    unsigned int z=2718281828;/*[0,4294967295]*/
    printf("x=%d\n",x);
    printf("y=%d\n",y);
    printf("z=%d\n",z);
    system("pause");
    return 0;
}