/*Day 5, convert exponential numbers to float numbers*/
#include <stdio.h>
#include <stdlib.h>

int main(){
    double p,q,r,s;
    p=-9.5e-4;
    q=3.78e+5;
    r=5.13e-2;
    s=6.1732e+12;
    printf("p=%f\n",p);
    printf("q=%f\n",q);
    printf("r=%f\n",r);
    printf("s=%f\n",s);
    system("pause");
    return 0;
}